    uint8_t UserRxBufferFS[MaxCommandsInBuffer][APP_RX_DATA_SIZE];//it could save <MaxCommandsInBuffer> number of commands
    uint8_t CommandsLens[MaxCommandsInBuffer]; //save the len of each command
